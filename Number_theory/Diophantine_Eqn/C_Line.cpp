// Linear Diophantine Equation ax + by = c;
int extgcd(int a, int b, int& x, int& y) {
    if (b == 0) {
        x = 1;
        y = 0;
        return a;
    }
    int x1, y1;
    int d = extgcd(b, a % b, x1, y1);
    x = y1;
    y = x1 - y1 * (a / b);
    return d;
}
void solve(){
    int a , b , c ;
    cin >> a >> b >> c ;
    c  = -c;
    int xg , yg;
    int g = extgcd(a , b , xg , yg);
    if(c % g){
        cout << -1 << sad;
    }
    else{
        int x = xg * (c / g);
        int y = yg * (c / g);
        cout << x << " " << y << sad;
    }
}
