const long long N = 1e6 + 10;
bool prime[N];
void sieve()
{
    prime[1] = prime[0] = true;
    for (long long i = 3; i * i <= N; i += 2)
    {
        if (prime[i])
        {
            continue;
        }
        for (long long j = i * i; j < N; j += i + i)
        {
            prime[j] = true;
        }
    }
}
bool isprime(long long x)
{
    if (x == 2)
        return true;
    else if ((x % 2 == 0))
        return false;
    return !prime[x];
}
vector<long long>store(N , 0);
void mini(){
    store[1] = 1;
    for(long long i = 2; i < N ; i++){
        if(store[i] == 1e7){
            for(long long j = i ; j < N ; j+=i){
                store[j] = min(store[j] , i);
            }
        }
    }
}
void solve(){
    long long n ;
    cin >> n;
    mini();
    while(n--){
        long long x , y;
        cin >> x >> y;
        if(y - x == 1){
            minusone;
            continue;
        }
        long long ans = 0 ;
        for(long long i = x; i <= y ; i++){
            if(store[x] == store[i]) break;
            ans++;
        }
        cout << ans << sad;
    }
}
