long long gcd(long long x, long long y)
{
    if (y == 0)
        return x;
    return gcd(y, x % y);
}
long long lcm(long long x, long long y)
{
    return ((x * y) / gcd(x, y));
}
void solve(){
    long long n ;
    cin >> n;
    long long ans = 0 , chk = 1;
    const long long mod = 1e9+7;
    for(long long i = 1 ; ;i++){
        chk = lcm(chk , i);
        if(chk > n) break;
        ans = (ans + n / chk)% mod;
    }
    cout << (ans  + n ) % mod<< sad;
}
